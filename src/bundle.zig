// Emit sprite-mln.{json,png} + identical @2x copies (MapLibre requests @2x on
// HiDPI; a missing @2x sheet fails the whole sprite load).
pub fn emitSpriteMln(io: std.Io, a: std.mem.Allocator, catalog_dir: []const u8, css_name: []const u8, out_dir: []const u8, base: []const u8, soundings: []const []const u8) !sprite.Atlas {
    // The MapLibre style pipeline bakes the base (1x) sheet; @2x is a separate
    // (currently identical) copy the loader requires.
    const atlas = try spriteMlnBytes(io, a, catalog_dir, css_name, soundings, 1.0);
    for ([_][]const u8{ "", "@2x" }) |suffix| {
        const jn = try std.fmt.allocPrint(a, "{s}{s}.json", .{ base, suffix });
        const pn = try std.fmt.allocPrint(a, "{s}{s}.png", .{ base, suffix });
        try std.Io.Dir.cwd().writeFile(io, .{ .sub_path = try std.fs.path.join(a, &.{ out_dir, jn }), .data = atlas.json });
        try std.Io.Dir.cwd().writeFile(io, .{ .sub_path = try std.fs.path.join(a, &.{ out_dir, pn }), .data = atlas.png });
    }
    return atlas;
}

// Read + parse one cell (base .000 + sequential updates) from `dir`; null on
// any failure. The parsed cell allocates from smp_allocator (thread-safe).
fn readParseCell(io: std.Io, dir: std.Io.Dir, bpath: []const u8) ?engine.s57.Cell {
    const gpa = std.heap.smp_allocator;
    const base = dir.readFileAlloc(io, bpath, gpa, .unlimited) catch return null;
    defer gpa.free(base);
    if (base.len == 0) return null;
    var ups = std.ArrayList([]const u8).empty;
    defer {
        for (ups.items) |ub| gpa.free(ub);
        ups.deinit(gpa);
    }
    const stem = bpath[0 .. bpath.len - 4];
    var u: u32 = 1;
    while (u <= 999) : (u += 1) {
        const upn = std.fmt.allocPrint(gpa, "{s}.{d:0>3}", .{ stem, u }) catch break;
        defer gpa.free(upn);
        const ub = dir.readFileAlloc(io, upn, gpa, .unlimited) catch break;
        ups.append(gpa, ub) catch {
            gpa.free(ub);
            break;
        };
    }
    return engine.s57.parseCellWithUpdates(gpa, base, ups.items) catch null;
}

/// THE eager cell-coverage loader: walk an ENC_ROOT, parse each cell once (base +
/// updates via readParseCell), and capture its M_COVR coverage + bbox + cscl + name
/// + date. One entry per stem (a boundary cell shared by two districts loads once).
/// Coverage is copied into `a` and survives the cell's deinit. "" / a missing dir
/// yields none; cells with no M_COVR are skipped. The single load path composed by
/// the cross-pack context, the partition debug bake, and the compositor.
fn loadCells(io: std.Io, a: std.mem.Allocator, path: []const u8) []compose.LoadedCov {
    if (path.len == 0) return &.{};
    var dir = std.Io.Dir.cwd().openDir(io, path, .{ .iterate = true }) catch return &.{};
    defer dir.close(io);
    var out = std.ArrayList(compose.LoadedCov).empty;
    var seen = std.StringHashMap(void).init(a);
    var walker = dir.walk(a) catch return &.{};
    defer walker.deinit();
    while (walker.next(io) catch null) |entry| {
        if (entry.kind != .file or !std.mem.endsWith(u8, entry.path, ".000")) continue;
        const stem = std.fs.path.stem(std.fs.path.basename(entry.path));
        if (seen.contains(stem)) continue;
        const stem_d = a.dupe(u8, stem) catch continue;
        seen.put(stem_d, {}) catch {};

        var cell = readParseCell(io, dir, entry.path) orelse continue;
        defer cell.deinit();
        const cov = cell.mcovrCoverage(a); // copied into `a`; survives cell.deinit()
        if (cov.len == 0) continue;
        var b = [4]f64{ 1e9, 1e9, -1e9, -1e9 };
        for (cov) |rings| for (rings) |ring| for (ring) |p| {
            b[0] = @min(b[0], p.lon());
            b[1] = @min(b[1], p.lat());
            b[2] = @max(b[2], p.lon());
            b[3] = @max(b[3], p.lat());
        };
        out.append(a, .{
            .name = stem_d,
            .date = a.dupe(u8, cell.dsid.isdt) catch "",
            .cscl = cell.params.cscl,
            .coverage = cov,
            .bounds = b,
        }) catch {};
    }
    return out.toOwnedSlice(a) catch &.{};
}

